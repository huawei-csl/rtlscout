// ---------------------------------------------------------------------------
// LOCAL MODIFICATION (not in the upstream DR_RTL source):
//   The `encoder` (always @(*), ~L535) and `decoder` (always @(*), ~L134)
//   case statements had no `default`, so `data_out` was left unassigned for
//   unmapped codes and yosys inferred a *transparent latch*. The latch survives
//   as a behavioral `always @*` in the gate-level netlist, which OpenROAD's STA
//   Verilog reader rejects (STA-0171) — making the design unmeasurable.
//   Fix: added explicit `data_out = 0;` defaults to both blocks → pure
//   combinational logic, no latch. This mirrors the SpireHDL variant, whose
//   encoder/decoder already use a Const(0) fallback for unmapped codes.
//   Functionally identical for all valid 8b/10b codes (the latch's hold path is
//   never exercised by valid input); only the baseline PPA differs from the
//   unmodified upstream RTL, so the pcie baseline must be re-measured.
// ---------------------------------------------------------------------------
module top(input clk,rst,s_en,d_en,m_piso,m_sipo,
           input [7:0]datain,
           output [7:0]dataout  
          );

wire[7:0]s_out,des_in;
wire[9:0]e_out,dec_in;
wire p_out;


//scrambler

scrambler SCR(.clk(clk),
               .rst(rst),
               .enable(s_en),
               .datain(datain),
               .dataout(s_out)
               );
//encoder

encoder ENC(.data_in(s_out),
             .rd(1'b1),
             .k(1'b0),
             .data_out(e_out)
                );

//PISO

piso PISO(.clk(clk),
          .rst(rst),
          .in(e_out),
          .mode(m_piso),
          .out(p_out)
          );
//SIPO

sipo SIPO(.clk(clk),
          .rst(rst),
          .in(p_out),
          .mode(m_sipo),
          .out(dec_in)
          );

//decoder

decoder DEC(.data_in(dec_in),
             .rd(1'b1),
             .k(1'b0),
             .data_out(des_in)
                );

// descrambler

descrambler DES(.clk(clk),
               .rst(rst),
               .enable(d_en),
               .data_in(des_in),
               .data_out(dataout)
               );

endmodule


//==========================================PCIe -- PISO DESIGN CODE===================================================

module piso #(parameter WIDTH=10)(input clk, input rst, input [WIDTH-1:0]in,input mode,output reg out);
reg [WIDTH-1:0]temp;

always@(posedge clk or negedge rst)
begin
        if(!rst)
               begin
                    temp<=0;  
                    out<=0;
               end
           
            else
                begin
                if(mode)
                    temp<=in;
                    else
                        begin
                              out<=temp[0];
                              temp<={1'b0,temp[WIDTH-1:1]};
                               
                        end
                 end       
end
endmodule


//=========================================================PCIe --SIPO DESIGN CODE===============================================================

module sipo#(parameter width=10)(input clk, input rst, input in, input mode,output reg [width-1:0]out);
reg [width-1:0]temp;

always @ (posedge clk or negedge rst)
begin
    if (!rst) begin
        temp <= 0;
        out  <= 0;
    end else if (mode) begin
        temp <= {in, temp[width-1:1]};
    end else begin
        out <= temp;
    end
end
endmodule


