//==================================================PCIe 16 -Bit SCRAMBLER DESIGN CODE===================================================================
module scrambler(
                    input clk,
                    input rst,
                    input enable,
                    input [7:0]datain,
                    output reg [7:0]dataout 
                 
                );

reg [15:0]lfsr;
reg [15:0]temp_lfsr;
reg[7:0]scrambled_data;
integer i;
reg feedback;

always @(*)
begin
    temp_lfsr=lfsr;
    scrambled_data=8'b0;
    
      for(i=0;i<8;i=i+1)
          begin
            scrambled_data[i]=datain[i] ^ temp_lfsr[15];

            feedback=temp_lfsr[15] ^ temp_lfsr[4] ^ temp_lfsr[3] ^ temp_lfsr[2];

            temp_lfsr={temp_lfsr[14:0],feedback};
         
          end
end

always @(posedge clk or negedge rst)
begin
    if(!rst)
        begin
            lfsr<=16'b1100_1010_0001_1110;
            dataout <= 8'b0;
        
        end
           
       else
           if(enable)
               begin
                lfsr<=temp_lfsr;
                dataout<=scrambled_data;
      
               end

              else 
                  begin
                    lfsr<=lfsr;
                    dataout<=dataout;
             
                  end

end
