// ==================================== PCIe 16-bit DESCRAMBLER  DESIGN CODE==============================================

module descrambler (
    input              clk,         
    input              rst,       
    input              enable,      // high when data_in is valid
    input       [7:0]  data_in,     // scrambled data input
    output reg  [7:0]  data_out     
);

    reg [15:0] lfsr;
    reg [15:0] temp_lfsr;
    reg [7:0]  outb;

    integer i;
    reg feedback;

     // Default PCIe seed 
      localparam [15:0] SEED = 16'b1100_1010_0001_1110;

      always @(posedge clk or negedge rst) begin
        if (!rst) begin
            lfsr <= SEED;
            data_out <= 8'h00;
           
        end else begin
          
            if (enable) begin
               temp_lfsr = lfsr;


               
                for (i = 0; i < 8; i = i + 1) begin
                    // descramble bit = scrambled_bit XOR LFSR MSB
                    outb[i] = data_in[i] ^ temp_lfsr[15];

                    // update LFSR
                    feedback = temp_lfsr[15] ^ temp_lfsr[4] ^ temp_lfsr[3] ^ temp_lfsr[2];
                    temp_lfsr = {temp_lfsr[14:0], feedback};
                end

                data_out <= outb;
               
                lfsr <= temp_lfsr;
            end
        end
    end
endmodule


